public class Operators {
    public static void main(String[] args) {
      
      // declare variables
      double a = 6, b = 17.32;
      double c = 0;
  
      // addition operator
      System.out.println("a + b / 2 = ");
      c = (a + b) / 2;
      System.out.println (c);
  
      // subtraction operator
      System.out.println("a - b = " + (a - b));
  
      // multiplication operator
      System.out.println("a * b = " + (a * b));
  
      // division operator
      System.out.println("a / b = " + (a / b));
  
      // modulo operator
      System.out.println("a % b = " + (a % b));
    }
  }